#Breve historia de Python:
# Python fue creado hacia finales de los años 80s y su implementación comenzó en diciembre de 1989 cuando Guido Van Rossum que 
# trabajaba en CWI, centro de investigación holandés de carácter oficial que, entre otras cosas, actualmente alberga la oficina 
# central de W3C, decidió empezar el proyecto dándole continuidad al lenguaje de programación ABC del que había formado parte 
# del equipo de desarrollo de CWI. 

#El nombre Python viene dado por la afición de Van Rossum al grupo Monty Python y fue originalmente desarrollado para 
# el sistema operativo Amoeba.

#Versiones:
# Hay 3 versiones, la mas reciente la versión 3. 
# La transición de Python 2 a Python 3 ha resultado mucho más costosa de lo esperado, debido a que Python 3 introdujo muchos cambios 
# en el lenguaje y obligaba a reescribir prácticamente todos los programas (aunque se han creado herramientas para ayudar en ese proceso).

# Python cuenta con un gran número de bibliotecas, cuyo repositorio oficial es PyPI (Python Package Index).

# En julio de 2018, Guido van Rossum anunció su retirada como BDOF.


