# QUE ES PYTHON:
# Python es un lenguaje de programación interpretado cuya filosofía hace hincapié en la legibilidad de su código.
# Es un leguaje multiparadigma por que pose:
# 1. Programación Orientado a Objetos, 2. Programación Interactiva y 3. Programación Funcional

# Las características del lenguaje son las siguiente:
#   - Simplicidad: ¡La gran fortaleza de Python!  
#   - Sintaxis clara: La sintaxis de Python es muy clara, es obligatoria la utilización de la indentación en todo el código que se escribe. Gracias a esta característica todos los programas 
#   escritos en Python tienen la misma apariencia.  
#   - Propósito general: Se pueden crear todo tipo de programas, incluyendo páginas web.  
#   - Lenguaje interpretado: Al ser un lenguaje interpretado no es necesario compilarlo, lo que te ahorrará tiempo a la hora de desarrollar. También implica que su ejecución sea más lenta, 
#   ya que los programas son ejecutados por el intérprete de Python en vez de ejecutados por la máquina donde lo arrancas.  
#   - Lenguaje de alto nivel: No es necesario que te preocupes de aspectos de bajo nivel como puede ser el manejo de la memoria del programa.  
#   - Lenguaje orientado a objetos: Lenguaje construido sobre objetos que incorporan datos y funcionalidades.  
#   - Open Source: Python ha sido portado a los diferentes sistemas operativos, por lo que puedes usarlo en el que más te guste. Otra característica de ser Open Source es que es un 
#   lenguaje de programación gratuito.  
#   - Extensas librerías: Facilitan la programación al incorporar mediante librerías una gran cantidad de funcionalidades.  
#   - Incrustable: Es posible añadir programas escritos en Python a programas escritos en C y C++.

# ¡Python es un lenguaje de programación muy completo! De todas las características que tiene, la clave de su gran éxito es la primera de ellas, la simplicidad con la que cuenta, que lo hace 
# perfecto para empezar en el mundo de la programación.

# Se debe elegir Python por su sencilles(creado para principiante, sintaxys fácil de comprender), codigo abierto(gratuito y código abierto desde hace 20 años),
# Popular(Es popular y se crean miles de librerías, módulos, códigos y programas), multidisciplinario(Varios usos entre Dllo Web, big data, IA, videojuegos, finanzas, etc. Esto te permite
# elegir el sector que más te guste y la mejor oferta laboral).

# Ventajas de Python: Facilidad de uso.  Legibilidad de código.  Facilidad de escritura de código asíncrono.  Muchas bibliotecas disponibles.
# Facilidad de uso.  Legibilidad de código.  Facilidad de escritura de código asíncrono.  Muchas bibliotecas disponibles.

# Desventajas de Python: Tiene mas ventajas que desventajas pero estas son: Curva de aprendizaje: la curva de aprendizaje en la parte web no es fácil.  Hosting: la mayoría de 
# los servidores no tienen soporte a Python, pero si lo soportan, la configuración tampoco es sencilla.  
# Librerías incluidas: algunas librerías incluidas no son del gusto de la comunidad y prefieren librerías de terceros.


