# Por qué Python?
# Existen multitud de razones por las cuales debes de aprender a programar en Python.

# Simplicidad:
# - La característica principal es Python es que es un lenguaje simple, reduce considerablemente el número de líneas de código en comparación con otros lenguajes y 
# provee herramientas para realizar operaciones de forma más simple que como se realizan con otros lenguajes. 
# - Por la simplicidad los errores que un programador pueda cometer cuando realiza los programa se ven reducidos, ya que al tener que escribir menos código fuente 
# se verá reducida la probabilidad de cometer errores.
# - Al escribir menos líneas de código el tiempo de desarrollo se ve reducido, y ésto es algo muy importante a tener en cuenta cuando se realizan proyectos de desarrollo de software.

# Resultados rápidos:
# - Con Python vas a poder ver los resultados de forma inmediata.
# - Python va a permitirte estar haciendo programas a los pocos días (incluso horas) de haber empezado, observarás que avanzas casi sin esfuerzo a gran velocidad.

# Punto de partida:
# - Python es un lenguaje muy completo, no pienses que por ser simple es un lenguaje básico. Con Python vas a aprender todos los conceptos existentes en el mundo de la programación.

